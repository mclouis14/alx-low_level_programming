#include <stdio.h>
/**
 * add - Adds two integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: Result of addition.
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - Subtracts two integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: Result of subtraction.
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - Multiply two integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: Result of multiplication.
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - Divides two integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: Result of division.
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - Mods two integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: Result of modulus.
 */
int mod(int a, int b)
{
	return (a % b);
}
